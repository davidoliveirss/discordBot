logger = get_logger()

cooldowns_suport = {}

def update_key_status(user_id, code):
    connection = create_connection()
    if connection:
        cursor = connection.cursor()
        try:
            query = """UPDATE license_keys SET valid=1 WHERE code=%s;"""
            cursor.execute(query, (code,))
            connection.commit()
            logger.info(f"Status of code {code} updated successfully, verified by ({user_id}).")
        except Error as e:
            logger.info(f"Couldn't update the code {code} status: {e}")
            return False
        finally:
            cursor.close()
            connection.close()
    return False

def get_user_id_by_key(code):
    connection = create_connection()
    if connection:
        cursor = connection.cursor()
        try:
            query = """SELECT id_user FROM license_keys WHERE code = %s LIMIT 1;"""
            cursor.execute(query, (code,))
            result = cursor.fetchone()
            if result:
                return result[0]
            else:
                return None
        except Error as e:
            logger.info(f"Error while searching for user id: {e}")
            return None
        finally:
            cursor.close()
            connection.close()
    return None

def check_verification_code(user_id, input_code):
    connection = create_connection()
    if connection:
        cursor = connection.cursor()
        try:
            query = """SELECT code, time_expire FROM verification_codes WHERE id_user = %s AND valid = TRUE ORDER BY time_created DESC LIMIT 1"""
            cursor.execute(query, (user_id,))
            result = cursor.fetchone()

            if result:
                stored_code, time_expire = result
                if stored_code == input_code and datetime.utcnow() < time_expire:
                    return True
                else:
                    logger.info(f"Code expired or invalid for user {user_id}.")
        except Error as e:
            logger.info(f"Couldn't verify the code: {e}")
        finally:
            cursor.close()
            connection.close()
    return False

def insert_verification_code(user_id, code, time_expire):
    connection = create_connection()
    if connection:
        cursor = connection.cursor()
        try:
            query = """
                INSERT INTO license_keys(id_user, code, valid, time_created, time_expire)
                VALUES (%s, %s, %s, NOW(), %s)
            """
            cursor.execute(query, (user_id, code, True, time_expire))
            connection.commit()
            logger.info(f"Code {code} generated by ({user_id}).")
        except Error as e:
            logger.info(f"Couldn't insert into db: {e}")
        finally:
            cursor.close()
            connection.close()

def generate_secure_code(user_id):
    characters = string.ascii_uppercase + string.digits
    code = '-'.join(
        ''.join(secrets.choice(characters) for _ in range(4))
            for _ in range(3)
    )
    time_expire = datetime.utcnow() + timedelta(days=30)
    insert_verification_code(user_id, code, time_expire)
    return code

async def give_role(user, role_id):
    guild = user.guild
    role = discord.utils.get(guild.roles, id=role_id)
    if role:
        await user.add_roles(role)

